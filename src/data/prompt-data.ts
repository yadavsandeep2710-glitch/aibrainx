// ===== Helper Functions =====
export function getPromptsByCategory(categorySlug: string): AIPrompt[] {
    return prompts.filter(p => p.category === categorySlug);
}

export function getPromptCategory(slug: string): PromptCategory | undefined {
    return promptCategories.find(c => c.slug === slug);
}

export function getAllCategorySlugs(): string[] {
    return promptCategories.map(c => c.slug);
}

export function getExamplePrompts(categorySlug: string, count: number = 2): { title: string; description: string }[] {
    return prompts
        .filter(p => p.category === categorySlug)
        .slice(0, count)
        .map(p => ({ title: p.title, description: p.description }));
}
